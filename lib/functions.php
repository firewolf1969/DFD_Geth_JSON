function ping_server($server_string, $timeout) {
      
$server_string = preg_replace('#^https?://#', '', $server_string);

$server_info = explode(":", $server_string);

      if ( $fp = fsockopen($server_info[0],$server_info[1],$errCode,$errStr,$timeout) ) {   
	 return true;
      }
      else {
	 return false;
      } 
      fclose($fp);

}
